package chapter06;

public class _01_Method {

    // 메소드 정의
    public static void sayHello() {
        System.out.println("Hello!");
    }

    // 메소드 호출
    public static void main(String[] args) {
        System.out.println("메소드 호출 시작");
        sayHello();
        System.out.println("메소드 호출 끝");
    }

}
